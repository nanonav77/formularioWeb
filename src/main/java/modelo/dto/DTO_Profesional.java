package modelo.dto;

public class DTO_Profesional {
	
	private int id_profesion;
	private String actividad;
	private String areainvestigacion;
	private String institucion;
	private String departamento;
	private int anoinicio;
	private String pais;
	private int cedula;
	
	
	public int getId_profesion() {
		return id_profesion;
	}
	public void setId_profesion(int id_profesion) {
		this.id_profesion = id_profesion;
	}
	public String getActividad() {
		return actividad;
	}
	public void setActividad(String actividad) {
		this.actividad = actividad;
	}
	public String getAreainvestigacion() {
		return areainvestigacion;
	}
	public void setAreainvestigacion(String areainvestigacion) {
		this.areainvestigacion = areainvestigacion;
	}
	public String getInstitucion() {
		return institucion;
	}
	public void setInstitucion(String institucion) {
		this.institucion = institucion;
	}
	public String getDepartamento() {
		return departamento;
	}
	public void setDepartamento(String departamento) {
		this.departamento = departamento;
	}
	public int getAnoinicio() {
		return anoinicio;
	}
	public void setAnoinicio(int anoinicio) {
		this.anoinicio = anoinicio;
	}
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public int getCedula() {
		return cedula;
	}
	public void setCedula(int cedula) {
		this.cedula = cedula;
	}
	
	
}
